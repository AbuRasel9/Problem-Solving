/*
ফিবোনাচি সিরিজ (Fibonacci Series) উদাহরণ:
যদি n = 5 হয়, তবে সিরিজটি হবে: 0, 1, 1, 2, 3
সূত্র: পরবর্তী সংখ্যা = পূর্ববর্তী দুটি সংখ্যার যোগফল (next = first + second)
*/

void fibonacciSeries({required int n}) {
  int firstNum = 0;  // সিরিজের প্রথম সংখ্যা
  int secondNum = 1; // সিরিজের দ্বিতীয় সংখ্যা

  print("Fibonacci Series up to $n terms:");

  // n সংখ্যক বার লুপ চলবে (আগে এখানে ১০ ফিক্সড ছিল, এখন n ব্যবহার করা হয়েছে)
  for (int i = 0; i < n; i++) {
    // বর্তমান সংখ্যাটি প্রিন্ট করা
    print(firstNum);

    // পরবর্তী সংখ্যাটি হিসাব করা (পূর্ববর্তী দুটি সংখ্যার যোগফল)
    int next = firstNum + secondNum;

    // মানগুলো অদলবদল করা যাতে পরের বার নতুন সংখ্যা পাওয়া যায়
    firstNum = secondNum; // দ্বিতীয় সংখ্যাটি এখন প্রথম সংখ্যা হয়ে যাবে
    secondNum = next;     // নতুন যোগফলটি এখন দ্বিতীয় সংখ্যা হয়ে যাবে
  }
}
